    def __init__(self, symptomList, medicalDepartment, diseaseDefinition, diseaseCauses, diseaseDetection, diseaseTreatment, diseaseProgress, diseasePrecautions):
        self.symptomList = symptomList
        self.medicalDepartment = medicalDepartment
        self.diseaseDefinition = diseaseDefinition
        self.diseaseCauses = diseaseCauses
        self.diseaseDetection = diseaseDetection
        self.diseaseTreatment = diseaseTreatment
        self.diseaseProgress = diseaseProgress
        self.diseasePrecautions = diseasePrecautions
